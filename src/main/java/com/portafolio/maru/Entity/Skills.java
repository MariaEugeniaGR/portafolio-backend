package com.portafolio.maru.Entity;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity
public class Skills {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;
    private String nameSkill;
    private String fotoSkill;
    private int porcentaje;

    public Skills() {
    }

    public Skills(String nameSkill, String fotoSkill, int porcentaje) {
        this.nameSkill = nameSkill;
        this.fotoSkill = fotoSkill;
        this.porcentaje = porcentaje;
    }

    public int getId() {
        return id;
    }

    public void setIdSkill(int id) {
        this.id = id;
    }

    public String getNameSkill() {
        return nameSkill;
    }

    public void setNameSkill(String nameSkill) {
        this.nameSkill = nameSkill;
    }

    public String getFotoSkill() {
        return fotoSkill;
    }

    public void setFotoSkill(String fotoSkill) {
        this.fotoSkill = fotoSkill;
    }

    public int getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(int porcentaje) {
        this.porcentaje = porcentaje;
    }   
    
}
